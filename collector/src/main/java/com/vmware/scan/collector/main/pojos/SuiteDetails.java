package com.vmware.scan.collector.main.pojos;

public class SuiteDetails {
	private String suiteName;
	private String suiteStatus;
	private String suiteCATLink;
	
	public String getSuiteName() {
		return suiteName;
	}
	
	public void setSuiteName(String suiteName) {
		this.suiteName = suiteName;
	}
	
	public String getSuiteStatus() {
		return suiteStatus;
	}
	
	public void setSuiteStatus(String suiteStatus) {
		this.suiteStatus = suiteStatus;
	}

	public String getSuiteCATLink() {
		return suiteCATLink;
	}

	public void setSuiteCATLink(String suiteCATLink) {
		this.suiteCATLink = suiteCATLink;
	}
}
